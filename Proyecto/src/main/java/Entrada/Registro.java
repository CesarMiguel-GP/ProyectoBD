    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        String correo = txtGmailUsuario.getText();
        String contrasena = new String(Password.getPassword());
        String confirmarC = new String(txtCPassword.getPassword());
        String fechaN = txtFechaN.getText();
        String nombre = txtNombre.getText();
        String aPaterno = txtAPaterno.getText();
        String aMaterno = txtAMaterno.getText();
        String gen = "";
        Date fechaNacimiento = Validador.convertirFecha(fechaN);
        if (!Validador.InicializarRegistro(
                correo, contrasena, confirmarC, fechaN,
                nombre, aPaterno, aMaterno, Genero, lblError)) {
            return;
        }
        if (!Validador.validarNombre(nombre, lblError) ||!Validador.validarNombre(aPaterno, lblError) ||!Validador.validarNombre(aMaterno, lblError)) {
            return; 
        }
        if (!Validador.validarCorreo(correo, lblError)) {
            return;
        }

        if (!Validador.validarContrasena(contrasena,lblError)) {
            return;
        }
        
        if(!contrasena.equals(confirmarC)){
            Validador.mostrarError(lblError,"La contrase√±a no es diferente.");
            return;
        }
        if (fechaNacimiento == null) {
            Validador.mostrarError(lblError, "Formato de fecha inv√°lido. Use dd/MM/yyyy.");
            return;
        }
        lblError.setText("");
        if (rdMaculino.isSelected()) {
            gen = "Masculino";
        } else if (rdFemenino.isSelected()) {
            gen = "Femenino";
        } else if (rdOtro.isSelected()) {
            gen = "Otro";
        }
        Usuario UsuarioN = new Usuario(correo, nombre,aPaterno, aMaterno, fechaNacimiento, gen, contrasena);
        Validador.agregarUsuario(UsuarioN);
        Login lg = new Login();
        lg.setVisible(true);
        dispose();
    }//GEN-LAST:event_btnRegistrarActionPerformed

